public class Telephone {
    private String model;
    private String color;
    private Integer modelNamber;
    private float simKarte;
    private String languages;
    private int price;

    //----------GETTERS/SETTERS-------------//


    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Integer getModelNamber() {
        return modelNamber;
    }

    public void setModelNamber(Integer modelNamber) {
        this.modelNamber = modelNamber;
    }

    public float getSimKarte() {
        return simKarte;
    }

    public void setSimKarte(float simKarte) {
        this.simKarte = simKarte;
    }

    public String getLanguages() {
        return languages;
    }

    public void setLanguages(String languages) {
        this.languages = languages;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}

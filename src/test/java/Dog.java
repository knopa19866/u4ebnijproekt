public class Dog {

    private String name;

    private String vetirinary;

    private double weight;

    private float age;

    private String color;


    //----------------GETTERS/SETTERS___________________//


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getVetirinary() {
        return vetirinary;
    }

    public void setVetirinary(String vetirinary) {
        this.vetirinary = vetirinary;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public float getAge() {
        return age;
    }

    public void setAge(float age) {
        this.age = age;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
